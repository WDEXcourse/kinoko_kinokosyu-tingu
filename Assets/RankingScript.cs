public class RankingScript : MonoBehaviour
{
    int OneScore;
    int TwoScore;
    int ThreeScore;
    int FourScore;
    int FiveScore;
    public Text OneText;
    public Text TwoText;
    public Text ThreeText;
    public Text FourText;
    public Text FiveText;
    [SerializeField, Header("CountEnemy")]
    int point;
    //セーブ時のkey!
    string[] ranking = { "OneScore", "TwoScore", "ThreeScore", "FourScore", "FiveScore" };
    //ランキング設定用のrankingValue!
    int[] rankingValue = new int[5];
    [SerializeField, Header("ScoreText")]
    Text[] rankingText = new Text[5];
    //スタートの中でGet&SetRanking
    
    // Start is called before the first frame update

    void Start()
    {
        Debug.Log("kokokara");
        GetRanking();
        SetRanking(CountEnemyScript.CountEnemy);
    }

    void Update()
    {
        
    }
    void GetRanking()
    {
        for (int i = 0; i < ranking.Length; i++)
        {
            rankingValue[i] = PlayerPrefs.GetInt(ranking[i], 0);
        }
        Debug.Log("tyuukan");
    }
    void SetRanking(int _value)
    {
        for (int i = 0; i < ranking.Length; i++)
        {
            if (_value > rankingValue[i])
            {
                var change = rankingValue[i];
                rankingValue[i] = _value;
                _value = change;
            }
        }
        for (int i = 0; i < ranking.Length; i++)
        {
            PlayerPrefs.SetInt(ranking[i], rankingValue[i]) ;
            Debug.Log(rankingValue[i]+"dayo!");
            rankingText[i].text = rankingValue[i].ToString();
        }
        Debug.Log("owari");
        //OneText.text ="1 " + rankingValue[1] + "/n" ;
    }
}









































































































































































































































































































































































































































































































































































































































































































































































































































































































































